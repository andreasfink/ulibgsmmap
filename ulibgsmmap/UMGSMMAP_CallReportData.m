#import "UMGSMMAP_CallReportData.h"

@implementation UMGSMMAP_CallReportData


@synthesize	operationName;
@synthesize	monitoringMode;
@synthesize	callOutcome;
@synthesize	extensionContainer;


- (void) processBeforeEncode
{
	[super processBeforeEncode];
	[asn1_tag setTagIsConstructed];
	asn1_list = [[NSMutableArray alloc]init];
	if(monitoringMode)
	{
		monitoringMode.asn1_tag.tagNumber = 0;
		monitoringMode.asn1_tag.tagClass = UMASN1Class_ContextSpecific;
		[asn1_list addObject:monitoringMode];
	}
	if(callOutcome)
	{
		callOutcome.asn1_tag.tagNumber = 1;
		callOutcome.asn1_tag.tagClass = UMASN1Class_ContextSpecific;
		[asn1_list addObject:callOutcome];
	}
	if(extensionContainer)
	{
		extensionContainer.asn1_tag.tagNumber = 2;
		extensionContainer.asn1_tag.tagClass = UMASN1Class_ContextSpecific;
		[asn1_list addObject:extensionContainer];
	}
}


- (UMGSMMAP_CallReportData *) processAfterDecodeWithContext:(id)context
{
	int p=0;
	UMASN1Object *o = [self getObjectAtPosition:p++];
	if((o) && (o.asn1_tag.tagNumber == 0) && (o.asn1_tag.tagClass == UMASN1Class_ContextSpecific))
	{
		monitoringMode = [[UMGSMMAP_MonitoringMode alloc]initWithASN1Object:o context:context];
		o = [self getObjectAtPosition:p++];
	}
	if((o) && (o.asn1_tag.tagNumber == 1) && (o.asn1_tag.tagClass == UMASN1Class_ContextSpecific))
	{
		callOutcome = [[UMGSMMAP_CallOutcome alloc]initWithASN1Object:o context:context];
		o = [self getObjectAtPosition:p++];
	}
	if((o) && (o.asn1_tag.tagNumber == 2) && (o.asn1_tag.tagClass == UMASN1Class_ContextSpecific))
	{
		extensionContainer = [[UMGSMMAP_ExtensionContainer alloc]initWithASN1Object:o context:context];
		o = [self getObjectAtPosition:p++];
	}
	while(o)
	{
        /* ... */
        o = [self getObjectAtPosition:p++];
    }
	return self;
}

- (NSString *) objectName
{
	return @"CallReportData";
}
- (id) objectValue
{
	UMSynchronizedSortedDictionary *dict = [[UMSynchronizedSortedDictionary alloc]init];
	if(monitoringMode)
	{
		dict[@"monitoringMode"] = monitoringMode.objectValue;
	}
	if(callOutcome)
	{
		dict[@"callOutcome"] = callOutcome.objectValue;
	}
	if(extensionContainer)
	{
		dict[@"extensionContainer"] = extensionContainer.objectValue;
	}
	return dict;
}

- (UMASN1Object<UMGSMMAP_asn1_protocol> *)decodeASN1opcode:(int64_t)opcode
                                             operationType:(UMTCAP_Operation)operation
                                             operationName:(NSString **)xop
                                               withContext:(id)context
{
    return self;
}


@end

